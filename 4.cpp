#include<iostream>
using namespace std;

int main(){
    // ********* loops in c++ ********* //
    // ********** for loop, while loop , do-while loop ********** //
    // ******** for loop ******** //
    for (int i = 0; i <= 20; i++)
    {
        cout << i << endl;
    }


    // ************ while loop *********** //
    int j = 0;
    while ( j <= 5)
    {
        cout << j << endl;
        j++;
    }


    // ************* do while loop ***************** //
    int k = 50;
    do
    {
        cout << k << endl;
        k++;
    } while (k <= 55);
    
    
    
    return 0;
}